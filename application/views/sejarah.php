		<p>Sebelum tahun 1955 Polda Jabar di sebut KANTOR POLRI PROVINSI 
		JAWA BARAT disingkat KAPERO JAWA BARAT, sebutan ini berlangsung 
		sampai dengan kurang lebih sampai tahun 1959. Kemudian istilah ini di ubah 
		menjadi KANTOR POLISI KOMISARIS JAWA BARAT, yaitu dari tahun 1959-
		1964. Tahun 1965 istilah atau namanya di ganti lagi menjadi PANGDAK VII ( 
		Panglima Daerah Angkatan Kepolisian VII Jawa Barat ). Dan pada tahun 1972 
		berganti nama lagi menjadi KOMDAK VII ( Komando Daerah Kepolisian VII 
		Jawa Barat / Langlang Buana ).
		<p>Markas Kepolisian Daerah Jawa Barat pertama kali bertempat di JL. Braga No.135
		Bandung yang di bangun pada tahun 1917 oleh arsitek asal belanda yang bernama CP Wolf Shoemaker. 
		kemudian pada bulan juni tahun 1987 markas Kepolisian Daerah Jawa Barat pindah ke JL. Soekarno-Hatta NO 749 
		Dan di tempati sampai sekarang. Sebagai kekuatan perjuangan yang lahir dari rakyat, maka salah satu instansi
		pemerintahan ini di anggap sangat perlu di kenal secara luas oleh lapisan masyarakat, 
		tidak saja melalui hasil-hasil pelaksanaan tugasnya namun juga melalui pembaharuan tentang tugas pokok, tugas dan 
		fungsi peranannya, struktur serta wilayah tugasnya.Kepolisian Daerah Jawa lahir pada 1 juli pada tahun 1964.
